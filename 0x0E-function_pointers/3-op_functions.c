#include "3-calc.h"


/**
 * op_add - adds integers
 *
 * @a: first int
 * @b: second int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts integers
 *
 * @a: first int
 * @b: second int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies integers
 *
 * @a: first int
 * @b: second int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides integers
 * @a: first int
 * @b: second int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_modulo - gets remainder
 * @a: first int
 * @b: second int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
