#include <stdio.h>


/**
 * main - Incoming
 *
 * Description: Prints the alphabet in uppercase and lowercase.
 * Return: 0
 */
int main(void)
{
	char a = 'a';
	char A = 'A';

	while (a <= 'z')
	{
		putchar(a);
		a++;
	}

	while
		(A <= 'Z');
	{
		putchar(A);
		A++;
	}

	putchar('\n');
	return (0);
}
